							<h1>About Leeds Data Mill</h1>
							<p>Leeds Data Mill is led by Mark Barrett, an Open Data expert who created the first Open Data app to get to number 1 in the iTunes chart (using 42m pieces of data). He has also managed over 4,000 datasets at the NHS, and in 2013 was recognised as one of the fifty most innovative people in healthcare.</p>
						</header>
						<section>
							<p>As an active member of the Leeds data community, and a co-founder of Leeds Data Thing, <a href="http://barrettmark.com">Mark</a> saw for himself the wealth of expertise, talent, and enthusiasm for data within the city.</p>
							<p>His vision was to create an Open Data platform for Leeds &#8211; a central website holding Open Data from public and private sector &#8211; a first in the world, putting Leeds at the forefront of the movement. This meant removing the barriers that prevent organisations from uploading their data (costs, time, resource), and allowing others to access and develop this rich data in any way they want to.</p>
							<p>Leeds City Council has been a fantastic supporter of the project from the outset, and provided the initial investment and commitment needed to help make this happen. However, the project is a &#8220;citizen empowered&#8221; project that Mark and the wider Leeds data community can drive in a way that's by the city, for the city.</p>
