							<h2>What is Open Data?</h2>
							<p>Open Data is information that&#8217;s collected and placed online for anyone to access and use in creative and inspiring ways. This information can be shaped to make a genuine difference to the services we rely on and give us the insight we&#8217;ve never had before &#8211; this makes it an incredibly powerful resource. </p>
							<p>Our goal is to bring together in one location a wealth of data in open formats, from which people can discover new information and explore what happens throughout the city.</p>
							<h2>Who creates Open Data?</h2>
							<p>Open Data is created by us, as individuals and groups, through our actions. It can include information from sports venues, shopping outlets, charities and Leeds City Council, to name just a few. Data within the site will never contain personal identifiable information &#8211; protecting individuals is a core principle of the project.</p>
							<p>When information from different sectors is combined it becomes an amazingly rich resource because it helps us identify connections. It&#8217;s through these connections that we can begin to see solutions for improving our lives and the lives of others.</p>
							<h2>Time to take action</h2>
							<p>Leeds Data Mill is the platform for us all to understand each other, and the city of Leeds, like never before. That&#8217;s why we&#8217;re challenging programmers, developers and hackers in Leeds to take this Open Data to create insights, and pave the way to build applications and services that benefit the whole city. </p>
							<p><i>The data is open. The website <em><a href="https://github.com/LeedsDataMill">source code</a></em> will be made open. The <em><a href="https://drive.google.com/a/leedsdatamill.org/folderview?id=0B7SuqXTi55XLWGJUMGFwcUVueEU&usp=drive_web">documentation</a></em> is <em>open</em>. The <em><a href="https://trello.com/b/dV8iMJrH/leeds-data-mill">timelines</a></em> are open. The challenge is open.</i></p>
